package main

import (
	"fmt"
	"os"

	"github.com/urfave/cli/v2"
)

func main() {
	app := &cli.App{
		Name:  "powermeter",
		Usage: "power meter tool",
		Commands: []*cli.Command{
			&SplitCommand,
			&ServeCommand,
			// &APICommand,
		},
	}

	err := app.Run(os.Args)
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}
